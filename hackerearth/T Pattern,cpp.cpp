#include <iostream>

using namespace std;

int main() {
    int n;
    cin>>n;
    while(n--){
        int m;
        cin>>m;
        for(int i=1;i<=m;i++){
            for(int j=1;j<=2*m;j++){
                if(j<=i || j>(2*m-i)){
                    cout<<"*";
                }
                else cout<<"#";
            }
            cout<<endl;
        }
    }
}
